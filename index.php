    <p>HTML (Hypertext Markup Language) adalah bahasa markah standar yang digunakan untuk membangun struktur dan mengatur konten halaman web. HTML menggunakan elemen-elemen dan tag-tag untuk mendefinisikan elemen-elemen struktural dalam dokumen web.</p>
    <p>Dalam HTML, sebuah halaman web terdiri dari elemen-elemen seperti teks, gambar, link, tabel, formulir, dan banyak lagi. Setiap elemen didefinisikan dengan menggunakan tag HTML yang sesuai. Tag HTML umumnya terdiri dari sebuah tag pembuka (< tag>) dan tag penutup (< /tag>), di mana konten yang ingin ditampilkan ditempatkan di antara tag pembuka dan tag penutup.</p>
    <p>HTML adalah dasar dari setiap halaman web. Dengan menggunakan elemen-elemen HTML yang tepat dan struktur yang baik, kita dapat membuat halaman web yang terstruktur, mudah dibaca, dan mudah dipahami oleh mesin pencari dan pengguna. HTML juga merupakan fondasi bagi bahasa pemrograman web seperti CSS (Cascading Style Sheets) dan JavaScript, yang digunakan untuk mengatur tampilan dan interaksi di halaman web.</p>
</div>

<div id="content" class="css">
    <img src="CSS3.png" alt="Logo CSS" width="180px" height="180px" class="css1">
    <p>CSS (Cascading Style Sheets) adalah bahasa pemrograman yang digunakan untuk mengatur tampilan dan gaya suatu halaman web. Dalam pengembangan web, HTML digunakan untuk membangun struktur halaman, sementara CSS digunakan untuk mengatur bagaimana elemen-elemen HTML tersebut ditampilkan di browser.</p>
    <p>Dengan CSS, Anda dapat mengontrol berbagai aspek tampilan halaman web, seperti warna, ukuran, jenis font, margin, padding, posisi, dan animasi. CSS memungkinkan Anda untuk memisahkan tata letak (layout) dari konten, sehingga memungkinkan Anda membuat halaman web yang lebih dinamis dan menarik secara visual.</P>
    <P>CSS bekerja dengan menggunakan aturan gaya (style rules) yang terdiri dari selector dan deklarasi. Selector mengidentifikasi elemen HTML yang akan diberi gaya, sedangkan deklarasi berisi properti dan nilai yang menggambarkan gaya yang diterapkan pada elemen tersebut.</p>
</div>

<div id="content" class="js">
    <img src="js.png" alt="Logo JS" width="180px" height="180px" class="js1">
    <p>JavaScript (JS) adalah bahasa pemrograman yang berjalan di sisi klien (client-side) pada browser web. Dengan JavaScript, Anda dapat membuat halaman web yang interaktif dan responsif, memberikan pengalaman pengguna yang lebih dinamis.</p>
    <p>Dasar dari JavaScript adalah mengontrol perilaku elemen-elemen HTML dan memanipulasi konten halaman secara dinamis. JavaScript memungkinkan Anda untuk menangani interaksi pengguna, memvalidasi input, membuat animasi, mengubah konten halaman secara real-time, dan banyak lagi.</p>
    <p>JavaScript juga memiliki dukungan untuk pengolahan data asinkronus, penggunaan API, pengaturan waktu (timers), dan manipulasi CSS. Hal ini memungkinkan Anda untuk mengakses dan berkomunikasi dengan sumber daya eksternal, membuat animasi yang halus, dan menjadwalkan tugas-tugas tertentu.</p>
